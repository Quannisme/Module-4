package com.example.casestudy.entity.employee;

import org.springframework.format.annotation.DateTimeFormat;

import javax.persistence.*;
import javax.validation.constraints.NotNull;
import java.util.Date;

@Entity
@Table(name = "employee")
public class Employee {
    @Id
    @JoinColumn(name = "employee_id")
    private int id;
    @JoinColumn(columnDefinition = "varchar(45)" , name = "employee_name")
    private String name;
    @JoinColumn(name = "employee_birthDay")
    @DateTimeFormat(pattern = "dd/MM/yyyy")
    @NotNull
    private Date birthDay;
    @JoinColumn(columnDefinition = "varchar(45) " , name = "employee_id_card")
    private String idCard;
    @JoinColumn(name = "employee_salary")
    private Double salary;
    @JoinColumn(columnDefinition = "varchar(45)" , name = "employee_phone")
    private String phone;
    @JoinColumn(columnDefinition = "varchar(45)", name = "employee_email")
    private String email;
    @JoinColumn(columnDefinition = "varchar(45)" , name = "employee_address")
    private String address;
    @ManyToOne
    @JoinColumn(name = "position_id")
    private Position position;

    @ManyToOne
    @JoinColumn(name = "education_degree_id")
    private EducationDegree educationDegree;

    @ManyToOne
    @JoinColumn(name = "division_id")
    private Division division;

    @ManyToOne
    @JoinColumn(name = "username")
    private User user;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public Date getBirthDay() {
        return birthDay;
    }

    public void setBirthDay(Date birthDay) {
        this.birthDay = birthDay;
    }

    public String getIdCard() {
        return idCard;
    }

    public void setIdCard(String idCard) {
        this.idCard = idCard;
    }

    public Double getSalary() {
        return salary;
    }

    public void setSalary(Double salary) {
        this.salary = salary;
    }

    public String getPhone() {
        return phone;
    }

    public void setPhone(String phone) {
        this.phone = phone;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getAddress() {
        return address;
    }

    public void setAddress(String address) {
        this.address = address;
    }

    public Position getPosition() {
        return position;
    }

    public void setPosition(Position position) {
        this.position = position;
    }

    public EducationDegree getEducationDegree() {
        return educationDegree;
    }

    public void setEducationDegree(EducationDegree educationDegree) {
        this.educationDegree = educationDegree;
    }

    public Division getDivision() {
        return division;
    }

    public void setDivision(Division division) {
        this.division = division;
    }

    public User getUser() {
        return user;
    }

    public void setUser(User user) {
        this.user = user;
    }
}
